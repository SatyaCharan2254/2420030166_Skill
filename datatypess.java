package myproject;

public class datatypess {
	    public static void main(String[] args) {
	        int a = 10;
	        float b = 5.5f;
	        double c = 20.123;
	        char d = 'A';
	        boolean e = true;
	        
	        System.out.println("Integer: " + a);
	        System.out.println("Float: " + b);
	        System.out.println("Double: " + c);
	        System.out.println("Char: " + d);
	        System.out.println("Boolean: " + e);
	    }
	

}
