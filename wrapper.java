package myproject;

public class wrapper {
     public static void main(String args[]) {
    	 //autoboxing 
    	 int a = 10;
    	 Integer obj=a;
    	 System.out.println("Auto boxed integer:"+obj);
    	 float b = 10.6789f;
    	 float obj1=b;
    	 System.out.println("Auto boxed integer:"+obj1);
    	 char c = 'A';
    	 char obj2=c;
    	 System.out.println("Auto boxed integer:"+obj2);
    	 double d = 102839467;
    	 double obj3=d;
    	 System.out.println("Auto boxed integer:"+obj3);
     }
}
